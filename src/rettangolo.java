public class rettangolo {
    public static void main(String[] args) {
        //Rettangolo pieno di asterischi

        for (int i = 0; i < 4; i++ ) {
            for (int j = 0; j < 6; j++) {
                System.err.print("*");
            }

            System.err.println("");
        }


}
}
